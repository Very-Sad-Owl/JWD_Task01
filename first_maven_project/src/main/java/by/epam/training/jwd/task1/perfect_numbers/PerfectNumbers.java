package by.epam.training.jwd.task1.perfect_numbers;

class PerfectNumbers {
    static boolean isPerfect(int num){
        if (num <= 0) {
            return false;
        }
        int sum = 0;
        for (int i = 1; i * i <= num; i++) {
            if (num % i == 0) {
                sum += i;
                if (i * i != num) {
                    sum += num / i;
                }

            }
        }
        return sum - num == num;
    }
}
